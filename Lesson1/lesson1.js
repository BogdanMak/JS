let isRoadClear = confirm("Tell me, please is road clear?");
let light = prompt("Tell me color of the traffic lights!!!");
if (light === "green") {
    if (isRoadClear) {
        document.write ("GO!");
    }
    else {
        document.write ( "Wait, when cars will gone!!!");
    }
}
if (light === "yellow") {
    if (isRoadClear) {
        document.write ("Wait!");
    }
    else {
        document.write ( "Still wait!");
    }
}
if (light === "red") {
    if (isRoadClear) {
        document.write ("STOP!!!");
    }
    else {
        document.write ( "STOP!!!");
    }
}
if (light === "broked") {
    document.write ("You can do whatever you want!");
}









